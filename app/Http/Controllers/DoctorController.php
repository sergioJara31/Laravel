    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Doctor  $doctor
     * @return \Illuminate\Http\Response
     */
    public function edit(Doctor $doctor)
    {
        return view("hospital.doctors.edit",['doctor'=>$doctor]);//recibimos el id de la mascota para saber cual modificar
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Doctor  $doctor
     * @return \Illuminate\Http\Response
     */
    public function update(Doctorstore $request, Doctor $doctor)
    {
        $doctor -> update($request->validated());
        return back()->with('status','Doctor fue modificada con exito');
    }

    /**
